test_smoke_design_review_round_in_real_git_repo() {
  local project_root
  local fake_bin
  local script_path="$REPO_ROOT/review-loop/scripts/run-review-bg.sh"
  local check_script="$REPO_ROOT/review-loop/scripts/check-review.sh"
  local sentinel
  local output_path

  project_root="$(setup_git_project)" || return 1
  fake_bin="$(install_fake_codex "$(dirname "$project_root")")"
  sentinel="$project_root/.claude/review-loop-test-session.sentinel"
  output_path="$project_root/specs/reviews/design/round-1-codex-review.md"

  # Verify we are in a real git repo with a clean worktree
  (
    cd "$project_root" || exit 1
    git rev-parse --is-inside-work-tree >/dev/null 2>&1
  ) || fail "expected a real git repo" || return 1

  (
    cd "$project_root" || exit 1
    [[ -z "$(git status --porcelain)" ]]
  ) || fail "expected clean worktree" || return 1

  # Run design-review round 1
  (
    cd "$project_root" || exit 1
    PATH="$fake_bin:$PATH" "$script_path" design-review 1
  ) || return 1

  wait_for_file_content "$sentinel" "done" || return 1
  assert_file_exists "$output_path" || return 1

  # check-review should report DONE
  (
    cd "$project_root" || exit 1
    assert_exit_and_output 0 "DONE" "$check_script" test-session design-review 1
  ) || return 1

  # Verify git state is still valid (no corruption)
  (
    cd "$project_root" || exit 1
    git status >/dev/null 2>&1
  ) || fail "git status failed after review round" || return 1
}

test_smoke_hook_relative_path_resolves_from_project_root() {
  local project_root
  local hook_command
  local hook_script_path

  project_root="$(setup_git_project)" || return 1

  # Copy plugin into the project (simulating installation)
  cp -R "$REPO_ROOT/review-loop" "$project_root/review-loop"

  # Extract hook command value from hooks.json (match "command": "..." key, take last match)
  hook_command="$(
    awk -F'"' '/"command"[[:space:]]*:/ { print $4 }' "$project_root/review-loop/hooks/hooks.json" | tail -n 1
  )"
  [[ -n "$hook_command" ]] || fail "could not extract hook command from hooks.json" || return 1

  # The hook command should be a relative path — resolve it from project root
  hook_script_path="${hook_command%% *}"

  (
    cd "$project_root" || exit 1
    [[ -x "$hook_script_path" ]]
  ) || fail "hook script not executable at relative path [$hook_script_path] from project root" || return 1

  # Actually run the hook command from project root (no active session, should exit 0 cleanly)
  (
    cd "$project_root" || exit 1
    bash -c "$hook_command"
  ) || fail "hook command failed when run from project root" || return 1
}

