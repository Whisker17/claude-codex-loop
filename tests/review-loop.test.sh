wait_for_file_content() {
  local path="$1"
  local expected="$2"
  local attempts=50
  local content=""

  while ((attempts > 0)); do
    if [[ -f "$path" ]]; then
      content="$(<"$path")"
      if [[ "$content" == "$expected" ]]; then
        return 0
      fi
    fi
    sleep 0.1
    attempts=$((attempts - 1))
  done

  fail "timed out waiting for [$expected] in $path; last content was [$content]"
}

wait_for_file_exists() {
  local path="$1"
  local attempts=50

  while ((attempts > 0)); do
    if [[ -f "$path" ]]; then
      return 0
    fi
    sleep 0.1
    attempts=$((attempts - 1))
  done

  fail "timed out waiting for file: $path"
}

