#!/usr/bin/env bash

set -uo pipefail

REPO_ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
FAILURES=0
TMP_DIRS=()

log() {
  printf '%s\n' "$*"
}

fail() {
  log "FAIL: $*"
  return 1
}

pass() {
  log "PASS: $*"
}

make_tmpdir() {
  local dir
  dir="$(mktemp -d "${TMPDIR:-/tmp}/review-loop-test.XXXXXX")"
  TMP_DIRS+=("$dir")
  printf '%s\n' "$dir"
}

cleanup_tmpdirs() {
  local dir
  local pid_file
  local pid

  for dir in "${TMP_DIRS[@]-}"; do
    if [[ -d "$dir/project/.claude" ]]; then
      while IFS= read -r pid_file; do
        [[ -f "$pid_file" ]] || continue
        pid="$(<"$pid_file")"
        if [[ -n "$pid" ]]; then
          kill -- "-$pid" >/dev/null 2>&1 || true
          kill "$pid" >/dev/null 2>&1 || true
        fi
      done < <(find "$dir/project/.claude" -name 'review-loop-*.pid' -print 2>/dev/null)
    fi
    rm -rf "$dir"
  done
}

trap cleanup_tmpdirs EXIT

run_test() {
  local name="$1"
  shift

  if "$@"; then
    pass "$name"
  else
    FAILURES=$((FAILURES + 1))
  fi
}

assert_file_exists() {
  local path="$1"
  [[ -f "$path" ]] || fail "expected file to exist: $path"
}

assert_executable() {
  local path="$1"
  [[ -x "$path" ]] || fail "expected file to be executable: $path"
}

assert_contains() {
  local path="$1"
  local needle="$2"
  grep -Fq "$needle" "$path" || fail "expected [$needle] in $path"
}

assert_not_contains() {
  local path="$1"
  local needle="$2"
  if grep -Fq "$needle" "$path"; then
    fail "did not expect [$needle] in $path"
  fi
}

assert_equals() {
  local expected="$1"
  local actual="$2"
  [[ "$expected" == "$actual" ]] || fail "expected [$expected], got [$actual]"
}

assert_exit_and_output() {
  local expected_status="$1"
  local expected_output="$2"
  shift 2

  local output
  local status

  set +e
  output="$("$@" 2>&1)"
  status=$?

  [[ $status -eq $expected_status ]] || fail "expected exit $expected_status, got $status with output: $output"
  [[ "$output" == "$expected_output" ]] || fail "expected output [$expected_output], got [$output]"
}

write_file() {
  local path="$1"
  local content="$2"
  mkdir -p "$(dirname "$path")"
  printf '%s' "$content" > "$path"
}

setup_project() {
  local tmp_root
  tmp_root="$(make_tmpdir)"

  local project_root="$tmp_root/project"
  mkdir -p "$project_root/.claude" "$project_root/specs/reviews/design" "$project_root/specs/reviews/code"

  write_file "$project_root/specs/design.md" $'# Design\n\nReview loop design content.\n'
  write_file "$project_root/.claude/review-loop.local.md" $'---\nactive: true\nsession_id: test-session\nphase: design\nround: 1\nstarted_at: 2026-03-20T00:00:00Z\nbranch: review-loop/test-session\nbaseline_sha: ""\ntask: "Implement review loop"\n---\n'

  printf '%s\n' "$project_root"
}

install_fake_codex() {
  local bin_root="$1/bin"
  mkdir -p "$bin_root"

  cat > "$bin_root/codex" <<'EOF'
#!/usr/bin/env bash
set -euo pipefail

project_root=""
prompt="${*: -1}"
delay="${FAKE_CODEX_DELAY_SECONDS:-0}"
status="${FAKE_CODEX_EXIT_STATUS:-0}"

while (($#)); do
  case "$1" in
    -C)
      project_root="$2"
      shift 2
      ;;
    *)
      shift
      ;;
  esac
done

mkdir -p "$project_root/.claude"
printf '%s' "$prompt" > "$project_root/.claude/fake-codex-prompt.txt"
printf '%s' "$$" > "$project_root/.claude/fake-codex.pid"

if [[ "$delay" != "0" ]]; then
  sleep "$delay"
fi

if [[ "${FAKE_CODEX_SKIP_OUTPUT:-0}" != "1" ]] && output_path="$(printf '%s\n' "$prompt" | grep -Eo 'specs/reviews/[A-Za-z0-9._/-]+\.md' | tail -n 1)"; then
  if [[ "${FAKE_CODEX_CREATE_OUTPUT_DIR:-1}" == "1" ]]; then
    mkdir -p "$project_root/$(dirname "$output_path")"
  fi
  printf 'Generated by fake codex.\n' > "$project_root/$output_path"
fi

if [[ -n "${FAKE_CODEX_TOUCH_FILE:-}" ]]; then
  mkdir -p "$project_root/$(dirname "$FAKE_CODEX_TOUCH_FILE")"
  printf 'Generated source file.\n' > "$project_root/$FAKE_CODEX_TOUCH_FILE"
fi

exit "$status"
EOF

  chmod +x "$bin_root/codex"
  printf '%s\n' "$bin_root"
}

install_fake_setsid_failure() {
  local bin_root="$1/bin"
  mkdir -p "$bin_root"

  cat > "$bin_root/setsid" <<'EOF'
#!/usr/bin/env bash
exit 97
EOF

  chmod +x "$bin_root/setsid"
  printf '%s\n' "$bin_root"
}

wait_for_file_content() {
  local path="$1"
  local expected="$2"
  local attempts=50
  local content=""

  while ((attempts > 0)); do
    if [[ -f "$path" ]]; then
      content="$(<"$path")"
      if [[ "$content" == "$expected" ]]; then
        return 0
      fi
    fi
    sleep 0.1
    attempts=$((attempts - 1))
  done

  fail "timed out waiting for [$expected] in $path; last content was [$content]"
}

wait_for_file_exists() {
  local path="$1"
  local attempts=50

  while ((attempts > 0)); do
    if [[ -f "$path" ]]; then
      return 0
    fi
    sleep 0.1
    attempts=$((attempts - 1))
  done

  fail "timed out waiting for file: $path"
}

