test_check_review_reports_expected_statuses() {
  local project_root
  local check_script="$REPO_ROOT/review-loop/scripts/check-review.sh"
  local pid

  assert_file_exists "$check_script" || return 1
  project_root="$(setup_project)"

  (
    sleep 5
  ) &
  pid=$!
  write_file "$project_root/.claude/review-loop-test-session.pid" "$pid"

  (
    cd "$project_root" || exit 1
    assert_exit_and_output 2 "RUNNING" "$check_script" test-session design-review 1
  ) || return 1

  write_file "$project_root/.claude/review-loop-test-session.sentinel" "timeout"
  (
    cd "$project_root" || exit 1
    assert_exit_and_output 1 "TIMEOUT" "$check_script" test-session design-review 1
  ) || return 1

  write_file "$project_root/.claude/review-loop-test-session.sentinel" "done"
  (
    cd "$project_root" || exit 1
    assert_exit_and_output 3 "FAILED" "$check_script" test-session design-review 1
  ) || return 1

  write_file "$project_root/.claude/review-loop-test-session.sentinel" "failed"
  (
    cd "$project_root" || exit 1
    assert_exit_and_output 3 "FAILED" "$check_script" test-session design-review 1
  ) || return 1

  write_file "$project_root/specs/reviews/design/round-1-codex-review.md" $'Review exists.\n'
  write_file "$project_root/.claude/review-loop-test-session.sentinel" "done"
  (
    cd "$project_root" || exit 1
    assert_exit_and_output 0 "DONE" "$check_script" test-session design-review 1
  ) || return 1

  kill "$pid" >/dev/null 2>&1 || true
  wait "$pid" 2>/dev/null || true
}

test_kill_review_terminates_session_and_cleans_runtime_files() {
  local project_root
  local fake_bin
  local run_script="$REPO_ROOT/review-loop/scripts/run-review-bg.sh"
  local kill_script="$REPO_ROOT/review-loop/scripts/kill-review.sh"
  local pid_file
  local pid

  assert_file_exists "$run_script" || return 1
  assert_file_exists "$kill_script" || return 1

  project_root="$(setup_project)"
  fake_bin="$(install_fake_codex "$(dirname "$project_root")")"
  pid_file="$project_root/.claude/review-loop-test-session.pid"

  (
    cd "$project_root" || exit 1
    PATH="$fake_bin:$PATH" FAKE_CODEX_DELAY_SECONDS=30 "$run_script" code-implement 1
  ) || return 1

  local attempts=30
  while ((attempts > 0)) && [[ ! -f "$pid_file" ]]; do
    sleep 0.1
    attempts=$((attempts - 1))
  done
  assert_file_exists "$pid_file" || return 1
  pid="$(<"$pid_file")"
  kill -0 "$pid" >/dev/null 2>&1 || fail "expected background process $pid to be alive" || return 1

  (
    cd "$project_root" || exit 1
    "$kill_script" test-session
  ) || return 1

  sleep 0.2
  if kill -0 "$pid" >/dev/null 2>&1; then
    fail "expected process $pid to be terminated"
    return 1
  fi

  [[ ! -f "$project_root/.claude/review-loop-test-session.pid" ]] || fail "expected pid file to be removed" || return 1
  [[ ! -f "$project_root/.claude/review-loop-test-session.sentinel" ]] || fail "expected sentinel to be removed" || return 1
}

